//базовые математические операции
//сложение
function add(a, b) {
    return a + b;
}
//вычитание
function subtract(a, b) {
    return a - b;
}
//умножение
function multiply(a, b) {
    return a*b;
}
//деление
function divide(a,b) {
    return a/b;
}
console.log(add(3,6));
console.log(subtract(2,3));
console.log(multiply(-2,4));
console.log(divide(-6,4));